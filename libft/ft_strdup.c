#include "../includes/libft.h"

char	*ft_strdup(const char *s)
{
	char	*tab;
	int		i;

	i = 0;
	tab = (char *)(malloc(sizeof(char) * (ft_strlen(s) + 1)));
	if (!tab)
		return (NULL);
	while (s[i] != '\0')
	{
		tab[i] = s[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
